    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String UserName = JOptionPane.showInputDialog(rootPane, "Enter your UserName", "Enter UserName", QUESTION_MESSAGE);
String security_question="";
String security_answer="";
String sa="";
String password;
try
{
    connect();
    String q = "Select security_question, answer from user_credentials where UserName = '"+UserName+"';";
    rs = s.executeQuery(q);
    if (rs.next())
    {
        security_question = rs.getString("security_question");
        security_answer = rs.getString("answer");
        sa = JOptionPane.showInputDialog(rootPane, security_question, "Answer Security Question", QUESTION_MESSAGE);
        if (security_answer.equals(sa))
        {
            password = JOptionPane.showInputDialog(null, "Enter New Password");
            String q1 = "Update user_credentials set password = '"+password+"' where Username = '"+UserName+"';";
            s.executeUpdate(q1);            
            JOptionPane.showMessageDialog(null, "Password changed");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Incorrect Answer", "Incorrect Answer", ERROR_MESSAGE);
        }
    }
    else
    {
        JOptionPane.showMessageDialog(null, "No such UserName", "Invalid UserName", ERROR_MESSAGE);
    }
}
catch (Exception e)
{
    JOptionPane.showMessageDialog(null, e.getMessage(), "Error", ERROR_MESSAGE);
}
    }//GEN-LAST:event_jButton2ActionPerformed

