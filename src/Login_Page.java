    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String username = jTextField1.getText();
String Password = new String(jPasswordField1.getPassword());
if (username.isEmpty() || Password.isEmpty())
{
    JOptionPane.showMessageDialog(null, "Please Enter UserName and Password", "Empty Field", ERROR_MESSAGE);
}
else
{
    String q;
    try
    {
        connect();
        q = "Select user_info.ID, Name, Username, Password from user_info, user_credentials where user_info.ID = user_credentials.ID && UserName = '"+username+"';";
        rs = s.executeQuery(q);
        if(rs.next())
        {
            String p = rs.getString("Password");
            nm = rs.getString("Name");
            if (p.equals(Password))
            {
                JOptionPane.showMessageDialog(rootPane, "Welcome "+nm, "Welcome", PLAIN_MESSAGE);
                ID = rs.getInt("ID");
                new Dashboard().setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Incorrect Password", "Incorrect Password", ERROR_MESSAGE);
                jTextField1.setText("");
                jPasswordField1.setText("");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No such User Exsist!", "Invalid UserName", ERROR_MESSAGE);
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
    }
    catch (Exception e)
    {
        JOptionPane.showMessageDialog(null, e.getMessage(), "Error", ERROR_MESSAGE);
    }
}
    }//GEN-LAST:event_jButton1ActionPerformed

